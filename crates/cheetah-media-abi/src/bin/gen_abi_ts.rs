//! Generates TypeScript ABI constants from the Rust descriptor layouts.

use std::fs;
use std::path::Path;

use cheetah_media_abi::{AbiVersion, FrameDescriptor, MemoryDescriptor, PacketDescriptor};

macro_rules! emit_layout {
    ($lines:ident, $prefix:expr, $ty:ty) => {
        $lines.push(format!(
            "export const {}_SIZE = {};",
            $prefix,
            std::mem::size_of::<$ty>()
        ));
        $lines.push(format!(
            "export const {}_ALIGN = {};",
            $prefix,
            std::mem::align_of::<$ty>()
        ));
    };
}

macro_rules! emit_field {
    ($lines:ident, $prefix:expr, $ty:ty, $name:expr, $field:ident) => {
        $lines.push(format!(
            "export const {prefix}_OFFSET_{name} = {offset};",
            prefix = $prefix,
            name = $name,
            offset = std::mem::offset_of!($ty, $field)
        ));
    };
}

fn main() {
    let manifest_dir = env!("CARGO_MANIFEST_DIR");
    let out_path = Path::new(manifest_dir)
        .join("../..")
        .join("packages/web/src/abi-constants.ts");

    let mut lines: Vec<String> = Vec::new();
    lines.push("// Auto-generated by crates/cheetah-media-abi/src/bin/gen_abi_ts.rs".into());
    lines.push("// Do not edit by hand.".into());
    lines.push(String::new());

    let current = AbiVersion::CURRENT;
    lines.push(format!(
        "export const ABI_VERSION_MAJOR = {};",
        current.major
    ));
    lines.push(format!(
        "export const ABI_VERSION_MINOR = {};",
        current.minor
    ));
    lines.push(format!("export const ABI_VERSION = {};", current.to_u32()));
    lines.push(String::new());

    emit_layout!(lines, "MEMORY_DESCRIPTOR", MemoryDescriptor);
    emit_field!(
        lines,
        "MEMORY_DESCRIPTOR",
        MemoryDescriptor,
        "REGION",
        region
    );
    emit_field!(
        lines,
        "MEMORY_DESCRIPTOR",
        MemoryDescriptor,
        "OFFSET",
        offset
    );
    emit_field!(
        lines,
        "MEMORY_DESCRIPTOR",
        MemoryDescriptor,
        "LENGTH",
        length
    );
    emit_field!(
        lines,
        "MEMORY_DESCRIPTOR",
        MemoryDescriptor,
        "CAPACITY",
        capacity
    );
    emit_field!(
        lines,
        "MEMORY_DESCRIPTOR",
        MemoryDescriptor,
        "GENERATION",
        generation
    );
    emit_field!(lines, "MEMORY_DESCRIPTOR", MemoryDescriptor, "FLAGS", flags);
    lines.push(String::new());

    emit_layout!(lines, "PACKET_DESCRIPTOR", PacketDescriptor);
    emit_field!(
        lines,
        "PACKET_DESCRIPTOR",
        PacketDescriptor,
        "TRACK_INDEX",
        track_index
    );
    emit_field!(
        lines,
        "PACKET_DESCRIPTOR",
        PacketDescriptor,
        "PAYLOAD",
        payload
    );
    emit_field!(
        lines,
        "PACKET_DESCRIPTOR",
        PacketDescriptor,
        "SIDE_DATA",
        side_data
    );
    emit_field!(
        lines,
        "PACKET_DESCRIPTOR",
        PacketDescriptor,
        "PTS_MS",
        pts_ms
    );
    emit_field!(
        lines,
        "PACKET_DESCRIPTOR",
        PacketDescriptor,
        "DTS_MS",
        dts_ms
    );
    emit_field!(
        lines,
        "PACKET_DESCRIPTOR",
        PacketDescriptor,
        "DURATION_MS",
        duration_ms
    );
    emit_field!(lines, "PACKET_DESCRIPTOR", PacketDescriptor, "FLAGS", flags);
    emit_field!(lines, "PACKET_DESCRIPTOR", PacketDescriptor, "EPOCH", epoch);
    lines.push(String::new());

    emit_layout!(lines, "FRAME_DESCRIPTOR", FrameDescriptor);
    emit_field!(
        lines,
        "FRAME_DESCRIPTOR",
        FrameDescriptor,
        "TRACK_INDEX",
        track_index
    );
    emit_field!(
        lines,
        "FRAME_DESCRIPTOR",
        FrameDescriptor,
        "PAYLOAD",
        payload
    );
    emit_field!(lines, "FRAME_DESCRIPTOR", FrameDescriptor, "PLANES", planes);
    emit_field!(
        lines,
        "FRAME_DESCRIPTOR",
        FrameDescriptor,
        "SIDE_DATA",
        side_data
    );
    emit_field!(lines, "FRAME_DESCRIPTOR", FrameDescriptor, "WIDTH", width);
    emit_field!(lines, "FRAME_DESCRIPTOR", FrameDescriptor, "HEIGHT", height);
    emit_field!(lines, "FRAME_DESCRIPTOR", FrameDescriptor, "PTS_MS", pts_ms);
    emit_field!(
        lines,
        "FRAME_DESCRIPTOR",
        FrameDescriptor,
        "DURATION_MS",
        duration_ms
    );
    emit_field!(lines, "FRAME_DESCRIPTOR", FrameDescriptor, "FLAGS", flags);
    emit_field!(lines, "FRAME_DESCRIPTOR", FrameDescriptor, "EPOCH", epoch);
    lines.push(String::new());

    fs::create_dir_all(out_path.parent().unwrap()).unwrap();
    fs::write(&out_path, lines.join("\n")).unwrap();
    println!("wrote {}", out_path.display());
}
